// Get commentaries based on locale
export const getCommentaries = (locale, animalType, t) => {
  // Get translated animal info from i18n
  const animalName = t(`toolbox.animalRace.animals.${animalType}.name`) || animalType;
  const plural = t(`toolbox.animalRace.animals.${animalType}.plural`) || animalType + 's';
  const action = t(`toolbox.animalRace.actions.${animalType}`) || 'move';
  
  if (locale === 'en') {
    return getEnglishCommentaries(animalType, animalName, plural, action);
  }
  return getVietnameseCommentaries(animalType, animalName, plural, action);
};
